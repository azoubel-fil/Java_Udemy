package b_estrutura_condicional;

public class A_IfElse {

	public static void main(String[] args) {

		int numero = 101;

		if (numero > 0) {
		    System.out.println("É positivo.");
		} else if (numero < 0) {
		    System.out.println("É negativo.");
		} else {
		    System.out.println("É zero.");
		}
		
	}

}
